  <h1>Submissions</h1>
      
  <p>
    The International Conference on Informatics in Schools: Situation, Evolution and Perspectives (ISSEP)
    is a forum for researchers and practitioners in the area of Informatics education, in both primary and secondary schools.
    The conference provides an opportunity for educators and researchers to reflect upon the goals and objectives of
    this subject matter, its curricula, various teaching and learning paradigms, and topics, as well as the connections
    to everyday life — including the various ways of developing Informatics Education in schools.
    The conference focuses on the educational goals and objectives of Informatics or Computer Science as a subject matter
    in primary, secondary, and vocational education and their different realization in compulsory and voluntary courses.
  </p>



  <h2>Call for Contributions</h2>

  <p>
    Authors are invited to submit high-quality manuscripts reporting original unpublished research and recent developments
    in the topics related to the scope of the conference (see below). All papers will be double-blind peer-reviewed
    by Programme Committee members.
  </p>

  <p>There are four different submission categories:</p>

  <ol>
    <li>Full Papers, divided into two subcategories (up to 12 pages):
      <ul>
        <li>Research papers (e.g., empirical, qualitative, quantitative, theory building, research methods,
          comparative studies, transferability of methods and results from other disciplines)
        </li>
        <li>
          Best practice papers/country reports/experience reports
        </li>
      </ul>
    </li>
    <li>Short Papers, divided into two subcategories (up to 8 pages)
      <ul>
        <li>Position/discussion papers</li>
        <li>Work in progress</li>
      </ul>
    </li>
    <li>Posters (2-4 pages)</li>
    <li>Workshops and panel proposals (2-4 pages)</li>
    <li>Doctoral consortium application (2 pages)</li>
  </ol>

  <p>
    Accepted papers and poster abstracts will be included in the ISSEP 2022 local proceedings.
    Selected (top quality) full papers will be published in the Springer Lecture Notes in Computer Science (LNCS) series.
  </p>


    
  <h2>Conference scope</h2>

  <p>The main topics of the conference include but are not limited to:</p>

  <ul class="wp-block-list">
    <li>Pedagogies for informatics teaching and learning</li>
    <li>Teacher education in Informatics</li>
    <li>Learning/teaching resources for Informatics</li>
    <li>Digital literacy and computational thinking</li>
    <li>Connecting informatics education in formal, non-formal, and informal learning environments</li>
    <li>Informatics curricula, programs, and courses</li>
    <li>Informatics and digital literacy in contexts, for example, STEAM education</li>
    <li>Informatics education and digital citizenship</li>
    <li>Engagement and empowerment strategies</li>
    <li>Broadening participation and diversity</li>
    <li>Sustainable education</li>
    <li>Teaching AI</li>
    <li>Data literacy</li>
  </ul>

  <p>
    Work submitted to ISSEP should be novel and material that has been previously published should not be republished
    unless the work has been significantly revised. Note, however, that while novel work is highly valued,
    constructive replication of previous studies can also be a significant contribution, and a new interpretation or
    evaluation of previously published work can make a good contribution.
  </p>



